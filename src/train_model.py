"""
train_model.py
- Loads processed CSV from results/processed_data.csv (generated by data_preprocessing.py)
- Trains a Linear Regression model using sklearn
- Saves trained model to ../trained_model.pkl
- Saves a small training log file in ../results/train_log.json
Usage:
    python src/train_model.py --input "../results/processed_data.csv" --model "../trained_model.pkl" --test-size 0.2 --random-seed 42 --threshold 0.6
"""
import argparse, os, json
import pandas as pd
import numpy as np
from sklearn.linear_model import LinearRegression
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
import joblib

def main(args):
    inp = os.path.join(os.path.dirname(__file__), args.input)
    model_out = os.path.join(os.path.dirname(__file__), args.model)

    df = pd.read_csv(inp)
    if 'SOH' not in df.columns:
        raise RuntimeError("Processed file must include 'SOH' column")
    # target: real SOH values from the dataset
    y = df['SOH'].values
    # features: U1..U21 cell voltage measurements
    X = df.drop(columns=['SOH']).values

    # simple scaling
    scaler = StandardScaler()
    X_scaled = scaler.fit_transform(X)

    X_train, X_test, y_train, y_test = train_test_split(X_scaled, y, test_size=args.test_size, random_state=args.random_seed)

    model = LinearRegression()
    model.fit(X_train, y_train)

    # save model + scaler together
    save_obj = {"model": model, "scaler": scaler, "threshold": args.threshold}
    os.makedirs(os.path.dirname(model_out), exist_ok=True)
    joblib.dump(save_obj, model_out)

    # save train/test split indices and a small log
    log = {
        "n_samples": len(df),
        "n_features": X.shape[1],
        "test_size": args.test_size,
        "random_seed": args.random_seed,
        "threshold": args.threshold
    }
    log_path = os.path.join(os.path.dirname(__file__), "..", "results", "train_log.json")
    with open(log_path, "w") as f:
        json.dump(log, f, indent=2)
    print(f"Trained Linear Regression and saved to {model_out}")
    print(f"Train log saved to {log_path}")

if __name__ == "__main__":
    p = argparse.ArgumentParser()
    p.add_argument("--input", type=str, default="../results/processed_data.csv")
    p.add_argument("--model", type=str, default="../trained_model.pkl")
    p.add_argument("--test-size", type=float, default=0.2)
    p.add_argument("--random-seed", type=int, default=42)
    p.add_argument("--threshold", type=float, default=0.6, help="SOH threshold for classification")
    args = p.parse_args()
    main(args)
